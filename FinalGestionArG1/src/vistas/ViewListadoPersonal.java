    private void btnReincorporarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReincorporarActionPerformed

        // TODO add your handling code here:
        // Obteniendo la fila seleccionada
        int filaSeleccionada = tablaListadoInactivos.getSelectedRow();

        if (filaSeleccionada != -1) {
            // Obteniendo los datos de la fila seleccionada
            int idMiembro = (int) modelo2.getValueAt(filaSeleccionada, 0);

            // Activando el miembro
            miemData.activarMiembro(idMiembro);

            // Actualizando la tabla
            llenarTabla();
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila primero");
        }
    }//GEN-LAST:event_btnReincorporarActionPerformed

