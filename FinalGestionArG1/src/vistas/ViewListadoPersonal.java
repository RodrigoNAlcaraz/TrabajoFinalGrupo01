    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tablaListadoPersonal.getSelectedRow();

        if (filaSeleccionada != -1) {
            // Obteniendo los datos de la fila seleccionada
            int idMiembro = (int) modelo.getValueAt(filaSeleccionada, 0);

            // Activando el miembro
            miemData.eliminarMiembro(idMiembro);

            // Actualizando la tabla
            llenarTabla();
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila primero");
        }


    }//GEN-LAST:event_btnEliminarActionPerformed

    private void armarCabeceraTabla() {
        ArrayList<Object> filaCabecera = new ArrayList<>();
        filaCabecera.add("Legajo");
        filaCabecera.add("Apellido");
        filaCabecera.add("Nombre");
        filaCabecera.add("DNI");

        for (Object it : filaCabecera) {
            modelo.addColumn(it);
        }
        tablaListadoPersonal.setModel(modelo);

        for (Object it : filaCabecera) {
            modelo2.addColumn(it);
        }
        tablaListadoInactivos.setModel(modelo2);

    }

    private void llenarTabla() {

        limpiarTabla();
        List<Miembro> listaActivos = new ArrayList<Miembro>();
        List<Miembro> listaInactivos = new ArrayList<Miembro>();

        listaActivos = miemData.listarMiembro();
        listaInactivos = miemData.listarMiembrosInactivos();

        for (Miembro miembro : listaActivos) {
            modelo.addRow(new Object[]{miembro.getIdMiembro(), miembro.getApellido(), miembro.getNombre(), miembro.getDni()});
        }

        for (Miembro miembro : listaInactivos) {
            modelo2.addRow(new Object[]{miembro.getIdMiembro(), miembro.getApellido(), miembro.getNombre(), miembro.getDni()});
        }

    }

    private void limpiarTabla() {
        int filasModelo = modelo.getRowCount();
        for (int i = 0; i < filasModelo; i++) {
            modelo.removeRow(0);
        }

        int filasModelo2 = modelo2.getRowCount();
        for (int i = 0; i < filasModelo2; i++) {
            modelo2.removeRow(0);
        }
    }


